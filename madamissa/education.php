					<p id="pG">Démarche pédagogique
					</p>	
					<p id="pG">Chaque projet proposé est travaillé en amont avec les équipes éducatives, et varie pour s’adapter en fonction du milieu et de l’âge du public cible. Un projet s’étend sur une année scolaire minimum, afin que les jeunes disposent de suffisamment de temps pour en comprendre le sens et en embrasser l'étendue. L’année est jalonnée par un échéancier qui alterne les recherches et actions des jeunes, et les interventions de l’association (contexte local, exemple de développement mené, impact de l’action, analyse et réflexion).
					</p>					
					<p id="pG">Le principe de la démarche pédagogique repose toujours sur l’implication des jeunes dans une action concrète et mesurable, et implique une réflexion. Pour cela L’association dispose d’outils pédagogiques comme des photos, des supports vidéos, et pour chaque jeune, d’un "carnet de voyage du citoyen actif et solidaire" pour enregistrer les recherches, les actions, les résultats et l’évaluation du projet de l’année.
					</p>
					<p id="pG">Son édition pourra être conduite par les élèves du lycée professionnel d’imprimerie Saint Stanislas d'Osny (95) - projet formalisé avec l’association en janvier 2015.
