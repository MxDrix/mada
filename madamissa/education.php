					<p id="pG"> ÉDUCATION A MADAGASCAR
					</p><br/>
					<p id="pG">Madagascar souffre d'un taux d'illetrisme élevé (70% de la population) et d'un abandon scolaire précoce et massif, surtout dans les villages. Les conditions d’Éducation des enfants qui habitent en zones rurales sont limitées par :
					</p>
					<p id="pG">- leur environnement, qui pénalise parfois l’accès aux écoles (conditions climatiques, état des sentiers)
					</p>
					<p id="pG">- leurs conditions de vie extrêmement difficiles, qui nuisent à la concentration, au développement intellectuel, au suivi scolaire
					</p>
					<p id="pG">- l'illettrisme ou l'analphabétisme des parents, qui ne peuvent apporter un soutien concret dans les différentes matières (l'enfant ne peut alors multiplier les sources et modes de représentation du savoir, nécessaires au développement de la capacité d'abstraction)
					</p>
					<p id="pG">- l'insalubrité des structures scolaires, dépourvues de matériel
					</p>
					<p id="pG">- le niveau de formation et la rétribution aléatoires des enseignants, qui doivent parfois trouver d'autres sources de revenus et ne peuvent donc assurer leur poste
					</p><br/>
					<p id="pG">Pour répondre à ces problèmes majeurs, les associations MADA M’IZA et MADA'GASY TSARA ont placé l'Education au centre du programme de développement global. 
					</p>
					<p id="pG">Ampahantany est un exemple concret des difficultés économiques et sociales du pays, et, par son enclavement, des zones rurales. À l’arrivée des associations en 2009, il n’existait qu'un emploi salarié dans le village (un instituteur).  Dans ce contexte, l’accès à l’éducation était loin d’être un droit acquis, et seulement 7% des enfants scolarisables en bénéficiait. 
Le village est pourtant doté d’une école primaire publique, mais son état de délabrement avancé et son emplacement témoignent que l'enseignement n'est pas une priorité. 
					</p>
					<p id="pG">Cette école se compose d’une structure cimentée de deux salles de classe de 45 m² chacune, et d’un bâtiment attenant en matériaux locaux (donc rudimentaires). Le bâtiment "en dur", datant de près de dix ans et non entretenu par la population, se trouve aujourd’hui terriblement endommagé. Quant au bâtiment attenant, il est insalubre : la terre battue du sol est un nid de parasites (des puces qui se logent sous la peau pour y pondre) auxquels sont exposés les élèves immobiles. 
Le mobilier est insuffisant et cassé, blessant régulièrement les enfants. Les tableaux sont écaillés, et il est difficile pour les élèves d'y lire et donc de recopier une leçon. 
					</p>
					<p id="pG">La situation de l’école, juchée sur un plateau étroit et accidenté servant de chemin pour la population, ne permet pas d’envisager des latrines, une cour de récréation, une clôture, ou encore moins une extension. 
Ampahantany, chef-lieu de 6 hameaux alentours, devrait pourtant être en mesure d'en accueillir tous les enfants. Or certains hameaux sont éloignés de plus de 8 kilomètres. Aussi, c’est à l’âge moyen de 9 ans que se fait l'entrée à l’école, un lieu que les enfants abandonnent généralement 5 ans plus tard, découragés de se voir toujours en classe de CP. Le taux de redoublement est en effet de 58 %, et l'abandon à 14 ans (avec un niveau de CE1) concerne près de la totalité des élèves.
					</p>
					<p id="pG">Les structures actuelles se contentent du taux de scolarisation actuel, mais ne seraient pas en mesure d'accueillir un nombre plus conséquent d'élèves ; elles ne s'accordent pas, de fait, avec la notion de développement, et donc avec les objectifs des associations.
					</p>
					<p id="pG">Le programme Éducation s’installe dans la continuité des actions d’éducation réalisées depuis 2009 / 2010 : 
					</p>
					<p id="pG">- ouverture d’un dispositif d’insertion social et professionnel par alternance pour les jeunes de plus de 14 ans déscolarisés
					</p>
					<p id="pG">- soutien aux enseignants et aux élèves
					</p><br/>
					<p id="pG">DISPOSITIF D’INSERTION SOCIAL ET PROFESSIONNEL
					</p>
					<p id="pG">Ce dispositf s’adresse aux jeunes sortis du système scolaire, afin de les inciter à se construire un projet professionnel, se professionnaliser ou reprendre des études. 
					</p><br/>
					<p id="pG">Découverte des métiers 
					</p>
					<p id="pG">Pendant deux ans, les jeunes volontaires déscolarisés intègrent le dispositif “Découverte des métiers”, et alternent stages et classe. Ils ne perçoivent aucune indemnité, et seuls les fournitures, les frais liés à la scolarité et le repas du midi sont pris en charge par l’association. 
					</p><br/>
					<p id="pG">Résultats 2014 
					</p>
					<p id="pG">En 2013/2014 : 6 jeunes 
					</p>
					<p id="pG">En 2014/2015 : 3 jeunes et 3 jeunes du projet découverte des métiers ont pu intégrer le collège du village à la rentrée 2014. 
					</p>
					<p id="pG">Les autorités locales ont toujours facilité l'actions des associations sur place, et ce depuis leur arrivée. Ces instances ont déjà validé et appuyé le projet éducation, et confirmé leur engagement en choisissant le fokontany d’Ampahantany - parmi les 17 formant la commune d’Ambinanony - pour construire un collège avec un fond local de développement. Ce choix a été motivé par la reconnaissance professionnelle du projet et la relation de confiance établie. Aussi, malgré l’abandon quasi systématique de l’école après 14 ans, les autorités ratifient notre projet avec ce collège qui a ouvert sa première classe (de 6ème) en 2014. 
					</p>
					<p id="pG">L’apprentissage 
					</p><br/>
					<p id="pG">Depuis octobre 2013, 6 jeunes méritants du dispositif « Découverte des métiers » bénéficient d’un contrat d’apprentissage. Ce cursus, sur deux ans, permet aux jeunes de se professionnaliser dans un domaine précis, tout en percevant une indemnité de stage (15€ / mois, soit la moitié du SMIC malgache). À ce stade du projet, ils sont orientés vers des métiers courants du village, prévus dans le programme de développement de l’association. 
					</p>
					<p id="pG">Ces jeunes formés localement, sont à même de s’approprier le système communautaire du programme, d'en assurer la pérennité et le perfectionnement. Avec l'expérience et la détermination dont ils font preuve, ils seront les premiers acteurs du développement local.
					</p>
					<p id="pG">Résultats 2014 
					</p>
					<p id="pG">En 2013/2014 : 6 jeunes 
					</p>
					<p id="pG">En 2014/2015 : 4 jeunes (1 abandon - pour mariage - et une exclusion - pour vol et abandon de poste, à plusieurs reprises) 
					</p>
					<p id="pG">Soutien aux études
					</p><br/>
					<p id="pG">L'association soutient des jeunes dont les projets professionnels requièrent au minimum le B.E.P.C, et donc des études au collège. Ils bénéficient dès lors d’une prise en charge complète des frais nécessaires à la poursuite des études (frais de scolarité, fournitures, aide à l’alimentation, etc.). 
					</p>
					<p id="pG">Résultats 2014
					</p>
					<p id="pG">Zoé, scolarisée en 4ème dans un collège à 40 kilomètres d’Ampahantany, a bénéficié de ce soutien. Or, l’association locale (M’GT) a dû rompre son contrat en cours d’année : son père n’a pas été affecté le budget qui lui a été remis aux études de la jeune et Zoé, qui a très vite abandonné le collège. Cette expérience a permis de revoir les critères de soutien alloué aux études, et de les limiter au collège qui s’est récemment ouvert dans le village. Le suivi moral et financier n'en sera que plus efficace. Zoé a été intégrée dans le dispositif “Découverte des métiers”, mais a très vite abandonné. L’extrême difficulté de vie de cette famille explique cet abandon.
					</p>
					<p id="pG">Soutien étendu
					</p>
					<p id="pG">Jeannot et Jean-Boscot sont deux jeunes suivis et soutenus par l'association depuis 2007. Depuis la rentrée 2013, ils ont accepté, malgré leur âge avancé (19 et 21 ans), d’intégrer une classe de 2nde dans le lycée publique d'une commune du nord de la capitale. Florence, une jeune d’Ampahanatny (sérieuse dans le dispositif “découverte des métiers”) les a accompagnés (admise en 5ème). 
					</p>
					<p id="pG">Le bailleur de fonds, qui soutenait ce projet depuis 2007, s’était engagé jusq’à l’obtention du brevet des collèges. Donc, depuis l’an passé, seuls les fonds propres de l'association financent ce projet. 
					</p>
					<p id="pG">Les 3 jeunes vivent seuls. Un budget mensuel leur est remis, et Jeannot, désigné comme responsable, le gère. Il comprend les frais de scolarisation, la location de deux pièces de leur maison d’accueil (ils sont hébergés par une famille en extrême difficulté), l’alimentation et une très petite somme pour les imprévus ou comme argent de poche. 
					</p>
					<p id="pG">L’association veille à ce que les jeunes puissent s’alimenter, mais les conditions matérielles restent très basiques, pour ne pas dire "précaires" (ils n’ont à ce jour qu’une mousse usée sur le sol pour tout couchage, et une lampe d'appoint pour trois, secondée de bougies, pour réviser le soir venu). Pour autant, la motivation reste forte, et ils se contentent bon gré mal gré de leurs conditions de vie actuelles.
					</p>
					<p id="pG">Résultats de l’année scolaire 2013/2014 :
					</p>
					<p id="pG">. Jeannot et Jean Bosco passent en 1ère , et Florence en 4ème 
					</p>
					<p id="pG">Soutien aux enseignants
					</p>
					<p id="pG">La difficulté économique du pays empêche la régularité des versements des salaires des enseignants, et la population, gravement appauvrie, ne peut assumer les frais de scolarité. Par conséquent, les enseignants désertent l’école pour subvenir à leurs besoins en trouvant des emplois journaliers. L’association a décidé, depuis 2012, de soutenir jusqu’à l’autonomie du système de développement proposé les salaires des enseignants suppléants, afin de les maintenir en poste. D’autre part, dans le cadre du projet orientation, les enseignants du village dispensent les cours aux jeunes concernés chaque samedi. Ils sont donc indemnisés en fonction des heures de cours effectués. 
					</p>
					<p id="pG">Vers un complexe scolaire
					</p>
					<p id="pG">Actuellement, 39% des enfants sont scolarisés. La population est impliquée dans toutes les actions, et les autorités locales de la circonscription scolaire (CISCO) l’appuient. La signature d’une convention de partenariat les place dans le cadre de la mise en œuvre d’un programme expérimental, reconnu comme pilote pour la commune.
					</p>
					<p id="pG">Aujourd’hui, le nouveau gouvernement poursuit les objectifs de la réforme « Education pour Tous », et c’est dans ce contexte que le programme s’inscrit. La combinaison de l’axe matériel et de l’axe pédagogique est une stratégie d’action qui semble pertinente. Les nouvelles structures, dont la construction est prévue pour 2016, attestent l'établissement d’un complexe scolaire dans le chef-lieu et d’une harmonisation pédagogique sur l’ensemble du fokontany.
					</p>
					<p id="pG">Ce complexe scolaire sera composé de :  
					</p>
					<p id="pG">- une maternelle
					</p>
					<p id="pG">- une école primaire
					</p>
					<p id="pG">- un espace de restauration scolaire
					</p>
					<p id="pG">- un dispositif d’orientation pour les jeunes en difficulté
					</p>
					<p id="pG">- un centre de formation local pour les maîtres
					</p>
					<p id="pG">Les hameaux situés à plus de 5 km de l’école seront dotés d’une école annexe, avec des classes allant de la maternelle au CE2, et un espace de restauration. A partir du CM1, les enfants rejoindront l’école du chef-lieu
					</p>
					<p id="pG">Le premier axe consiste en la construction de structures aménagées, plus solides et en position plus centrale dans les hameaux concernés, afin de limiter les déplacements des élèves. Des latrines pour chaque structure permettront également un meilleur confort pour les élèves et le corps enseignant, et participeront à une diminution du taux de prévalence de certaines maladies chroniques, d’une diminution des risques d’épidémie (choléra, etc.).
					</p>
					<p id="pG">Le deuxième axe réside à adopter une pédagogie plus proche du mode de raisonnement et du cadre de vie des enfants. L’enseignement adapté, avec quelques pratiques de l’aide cognitive, semble tout à fait convenir au contexte local. Il a déjà été approuvé par l’équipe enseignante et les autorités locales lors de séances de travail.
					</p>
					<p id="pG">Tous les enseignants seront formés à l’approche des pratiques de l’Enseignement Adapté français, qui apportera les outils pédagogiques nécessaires à la cohérence du projet.
					</p>
					<p id="pG">
L’école maternelle du chef-lieu est un élément clef. Il y en a peu sur l'ensemble du pays, et se trouvent essentiellement en ville. L'absence d'une telle structure à Ampahantany, conjuguée au développement actuel des enfants, mettent en évidence l’importance de la préscolarisation. 

					</p>
					<p id="pG">Celle-ci va permettre aux enfants, dès l’âge de 3 ans, de bénéficier : 
					</p>
					<p id="pG">-  de l’éveil pédagogique indispensable avant d’aborder les apprentissages fondamentaux 
					</p>
					<p id="pG">-  d’un repas complet chaque jour de classe 
					</p>
					<p id="pG">-  d’un contrôle et d’un suivi médical 
					</p>
					<p id="pG">Les enfants qui auront suivi deux ou trois années de pré-scolarisation appréhendront les apprentissages fondamentaux avec plus d'aisance ; il faudra moins de 5 ans pour mesurer l’impact de la maternelle sur la suite du cursus scolaire. 
					</p>
					<p id="pG">Lorsqu’un foyer est démuni il lui est bien plus difficile de dispenser l'éveil auquel les pays occidentaux sont habitués. Aussi, on peut comprendre que l’éveil pédagogique, tel que nous l’entendons, n’est même pas envisagé dans le contexte de cette population rurale qui vit encore en dessous du seuil de pauvreté. 
					</p>
					<p id="pG">C’est pourquoi la scolarisation dans le village, ouverte à tous les enfants à partir 4 ans, permettra aux enfants d’exploiter au mieux leur potentiel intellectuel en installant, au plus tôt, les concepts mentaux indispensables à leur développement cognitif. Il optimisera de fait leurs chances d'une réussite scolaire et d’une qualité de vie meilleure. 
					</p>
					<p id="pG">L’école reprendra alors tout son sens et se replacera comme un des vecteurs majeurs de développement de ces populations défavorisées car <B>l'Education est bel et bien un moyen de réduire les inégalités</B>.
					</p>
					<p id="pG">Perennité du projet
					</p>
					<p id="pG">Sur place, le pilotage du projet est délégué à l’association MADA’GASY TSARA, dont les membres sont originaires de la région. La construction des nouvelles écoles, salubres et solides, permettra aux familles de se sentir revalorisées dans l’éducation de leurs enfants.
					</p>
					<p id="pG">La gestion sera confiée aux autorités compétentes, ainsi qu'à l’association des parents d’élèves (FRAM) et au comité de gestion locale. Grâce à la caisse de solidarité - alimentée par la coopérative et deux autres secteurs économiques en perspective -, l’entretien des écoles sera assumé et certaines bourses pourront être attribuées aux familles les plus nécessiteuses.
					</p>
					<p id="pG">ÉDUCATION EN FRANCE
					</p>
					<p id="pG">Depuis 2007, des projets pédagogiques élaborés et portés par des professeurs de l’Île de France ont engagé près de 5 000 élèves sur des actions de solidarité, dans une démarche réflexive de citoyenneté et d’ouverture au monde (les activités menées à Madagascar apportent des cas d’étude correspondant aux instructions officielles de l’Education Nationale pour aborder le développement durable). 
Un collège d’Île de France, partenaire de l’association depuis 2007, a ainsi constitué un « club Madagascar » dans lequel des élèves volontaires participent aux projets de l’association à travers diverses actions qu'ils organisent.

					</p>
					<p id="pG">Depuis peu, des chantiers internationaux constituent un projet interculturel, un lieu d'échange dont l’objectif général est de mettre en lumière cette démarche solidaire et citoyenne, tout en favorisant l’insertion sociale et professionnelle des jeunes en France et à Madagascar. Il engage des jeunes français sur des actions d’intérêt local, dont la première est programmée pour la fin de cette année civile avec la rénovation du centre de santé.
					</p>	
					<p id="pG">En proposant un lieu d’accueil dans le village malgache pour des jeunes volontaires français, les associations leur ouvrent, ici comme là-bas, la possibilité de contextualiser leurs engagements et de diversifier leurs représentations de ce qu'est la société.
					</p>
					<p id="pG">Depuis cette année scolaire, 2 lycées d’Argenteuil travaillent en partenariat avec l’association sur le projet E3D (Établissement en Démarche de Développement Durable) de l’Education Nationale. Des élèves en baccalauréat professionnel (Section Technologique Industriel Développement Durable), vont pouvoir valider des épreuves d’examens avec un chantier international au profit du village (électrification du centre de santé).
